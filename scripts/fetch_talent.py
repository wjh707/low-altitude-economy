def generate_mock_talent_data(min_per_position=3, max_per_position=15):
    """
    生成模拟人才招聘数据
    每个岗位随机生成 min_per_position 到 max_per_position 条招聘信息
    返回招聘信息列表
    """
    talents = []
    record_id = 1

    # 日期范围：2026年1月-4月
    date_start = datetime.date(2026, 1, 1)
    date_end = datetime.date(2026, 4, 30)

    for pos_name, pos_cat in ALL_POSITIONS:
        count = random.randint(min_per_position, max_per_position)
        salary_min, salary_max = SALARY_RANGES.get(pos_name, (15, 40))

        for _ in range(count):
            # 随机生成发布日期
            days_range = (date_end - date_start).days
            random_days = random.randint(0, days_range)
            pub_date = (date_start + datetime.timedelta(days=random_days)).isoformat()

            # 薪资在范围内随机浮动
            actual_min = salary_min + random.choice([-2, -1, 0, 0, 1, 2])
            actual_max = salary_max + random.choice([-2, -1, 0, 0, 1, 2, 3, 4])
            actual_min = max(actual_min, 8)
            actual_max = max(actual_max, actual_min + 3)

            # 根据岗位选择合理的学历要求
            if pos_cat == "研发制造类":
                edu = random.choices(["本科", "硕士", "博士"], weights=[3, 5, 2])[0]
            elif pos_cat == "战略规划类":
                edu = random.choices(["本科", "硕士", "博士"], weights=[2, 5, 3])[0]
            elif pos_cat == "操控培训类" or pos_name in ["无人机操控员", "装调维修工程师", "地面支持工程师"]:
                edu = random.choices(["大专", "本科"], weights=[5, 5])[0]
            elif pos_cat == "数据应用类":
                edu = random.choices(["本科", "硕士", "博士"], weights=[3, 5, 2])[0]
            elif pos_cat == "安全监管类":
                edu = random.choices(["本科", "硕士"], weights=[6, 4])[0]
            else:
                edu = random.choices(["大专", "本科", "硕士"], weights=[2, 6, 2])[0]

            # 经验要求
            if pos_cat == "战略规划类":
                exp = random.choices(["3-5年", "5-10年", "10年以上"], weights=[3, 5, 2])[0]
            elif pos_cat == "研发制造类":
                exp = random.choices(["1-3年", "3-5年", "5-10年"], weights=[2, 5, 3])[0]
            elif pos_name in ["无人机操控员", "装调维修工程师", "地面支持工程师"]:
                exp = random.choices(["1-3年", "3-5年"], weights=[6, 4])[0]
            else:
                exp = random.choices(["1-3年", "3-5年", "5-10年"], weights=[3, 5, 2])[0]

            # 描述
            desc_templates = DESCRIPTION_TEMPLATES.get(pos_name, ["负责相关岗位工作内容"])
            description = random.choice(desc_templates)

            # 是否在招（90%概率为True）
            is_active = random.random() < 0.9

            talent = OrderedDict([
                ("id", record_id),
                ("position", pos_name),
                ("category", pos_cat),
                ("company", random.choice(COMPANIES)),
                ("city", random.choice(CITIES)),
                ("salary_min", actual_min),
                ("salary_max", actual_max),
                ("education", edu),
                ("experience", exp),
                ("publish_date", pub_date),
                ("source", random.choice(SOURCES)),
                ("description", description),
                ("is_active", is_active),
            ])
            talents.append(talent)
            record_id += 1

    return talents


